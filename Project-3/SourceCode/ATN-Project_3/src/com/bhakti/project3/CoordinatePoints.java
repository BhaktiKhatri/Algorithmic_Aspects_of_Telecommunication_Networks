package com.bhakti.project3;

//Creates a point with (x,y) coordinates.
public class CoordinatePoints
{
	public int x;
	public int y;

	public CoordinatePoints(int x, int y) 
	{
		this.x = x;
		this.y = y;
	}

	public int getX() 
	{
		return x;
	}

	public void setX(int x) 
	{
		this.x = x;
	}

	public int getY() 
	{
		return y;
	}

	public void setY(int y) 
	{
		this.y = y;
	}

}
